function resizeMap() {
    const img = document.querySelector('img[usemap]');   // The image element with the usemap attribute
    const areas = document.querySelectorAll('area');      // All area elements in the map

    // Check if the image has loaded natural dimensions to avoid errors
    if (!img || !img.naturalWidth || !img.naturalHeight) return;

    const widthRatio = img.clientWidth / img.naturalWidth;   // Ratio of current width to original
    const heightRatio = img.clientHeight / img.naturalHeight; // Ratio of current height to original

    areas.forEach(area => {
        // Check if data-coords is defined before trying to split it
        if (area.dataset.coords) {
            const originalCoords = area.dataset.coords.split(','); // Get the original coordinates from the data-coords attribute
            const newCoords = originalCoords.map((coord, index) => {
                return index % 2 === 0
                    ? Math.round(coord * widthRatio)   // Adjust x coordinates
                    : Math.round(coord * heightRatio); // Adjust y coordinates
            });
            area.coords = newCoords.join(',');  // Set new adjusted coordinates
        }
    });
}

